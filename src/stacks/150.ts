function evalRPN(tokens: string[]): number {
  const operators: { [index: string]: Function } = {
    "+": (a: number, b: number) => b + a,
    "-": (a: number, b: number) => b - a,
    "*": (a: number, b: number) => b * a,
    "/": (a: number, b: number) => ~~(b / a),
  };

  const digitStack: number[] = [];

  for (let current of tokens) {
    if (Object.keys(operators).includes(current)) {
      const num = operators[current](digitStack.pop(), digitStack.pop());
      digitStack.push(num);
    } else {
      digitStack.push(+current);
    }
  }

  return digitStack[0];
}

